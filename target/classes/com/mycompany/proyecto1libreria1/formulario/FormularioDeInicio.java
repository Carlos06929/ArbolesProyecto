    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int codigo=Integer.parseInt(JOptionPane.showInputDialog("INGRESE El CODIGO DEL LIBRO: "));
        boolean existe;
         Ficheros existeFichero=new Ficheros();
                if(Entrada.eleccion==1) {
                    arbol=new ArbolBinarioBusqueda<>();
                    arbol=existeFichero.leerFicheroABB(arbol);
                }if (Entrada.eleccion==2) {
                    arbol=new AVL<>();
                    arbol=existeFichero.leerFicheroAVL(arbol);
                }if (Entrada.eleccion==3) {
                    arbol=new MVias<>();
                    arbol=existeFichero.leerFicheroMV(arbol);
                }if (Entrada.eleccion==4) {
                    arbol=new ArbolB<>();
                    arbol=existeFichero.leerFicheroAB(arbol);
                }
                    
                    existe=arbol.contiene(codigo);
                    if (existe==false){
                        REGISTRODELIBROS reg_libros=new REGISTRODELIBROS(codigo);
                        reg_libros.setVisible(true);
                        this.dispose();
                    }else{
                        arbol.buscar(codigo).setCopias(arbol.buscar(codigo).getCopias()+1);
                        JOptionPane.showMessageDialog(null,"El Libro ya esta registrado \n Se incremento el numero de ejemplares en 1");
                       switch (Entrada.eleccion) {
                        case 1:
                           existeFichero.escribirFicheroABB(arbol);
                            break;
                        case 2:
                            existeFichero.escribirFicheroAVL(arbol);
                            break;
                        case 3:
                            existeFichero.escribirFicheroMV(arbol);
                            break;
                        case 4:
                            existeFichero.escribirFicheroAB(arbol);
                            break;
                        default:
                            break;
                    }
                        
                    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

