    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        IArbolBusqueda<Integer,Pelicula> arbol = null;
        boolean EleccionValida=false;
        Ficheros manejoFichero=new Ficheros();
        if(abb.isSelected()){
            eleccion=1;
            arbol=new ArbolBinarioBusqueda<>();
            manejoFichero.escribirFicheroABB(arbol);
            EleccionValida=true;
        }if (avl.isSelected()) {
            arbol=new AVL<>();
            eleccion=2;
            manejoFichero.escribirFicheroAVL(arbol);
            EleccionValida=true;
        }if (mvias.isSelected()) {
            eleccion=3;
            arbol=new MVias<>();
            manejoFichero.escribirFicheroMV(arbol);
            EleccionValida=true;
        }if (aB.isSelected()) {
            
            eleccion=4;
            arbol=new ArbolB<>();
            manejoFichero.escribirFicheroAB(arbol);
            EleccionValida=true;
        }
         if (EleccionValida==false) {
            JOptionPane.showMessageDialog(null, "Eleccion invalida. \n Elija una de las opciones.");
            }else{
        rellenar_Arbol();
        FORMULARIO_INICIO formulario=new FORMULARIO_INICIO();
        formulario.setVisible(true);
        this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

